    public void check() throws FileNotFoundException, IOException {
        String pats = (String) pat_name.getSelectedItem();
        String pat[] = pats.split(",");
        id = pat[0];
        BufferedReader br2 = new BufferedReader(new FileReader("pres.csv"));
        String line2;
        while ((line2 = br2.readLine()) != null) {
            String values2[] = line2.split(",");
            if (values2[0].equals(id)) {
                prob.setText(values2[1]);
                pres.setText(values2[2]);
            }
        }
        br2.close();

    }

