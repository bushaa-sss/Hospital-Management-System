    public void update(String value[]) {
        System.out.println(value[0] + "," + value[1] + "," + value[2]);
        String line;

        try {
            File newfile = new File("temp.csv");
            File oldfile = new File("pres.csv");
            FileWriter fw = new FileWriter(newfile, true);
            BufferedWriter bw = new BufferedWriter(fw);
            PrintWriter pw = new PrintWriter(bw);

            BufferedReader br = new BufferedReader(new FileReader("pres.csv"));
            while ((line = br.readLine()) != null) {
                String values[] = line.split(",");
                if (values[0].equals(id)) {
                    pw.println(value[0] + "," + value[1] + "," + value[2]);
                } else {
                    pw.println(line);
                }

            }
            pw.flush();
            pw.close();
            br.close();
            bw.close();
            fw.close();
            System.out.println("updated");
            String xx = oldfile.getAbsolutePath();

            oldfile.delete();

            if (oldfile.exists()) {
                System.out.println("not deleted");
            }
            File dump = new File(xx);

            newfile.renameTo(dump);
            jLabel2.setVisible(true);
        } catch (IOException ex) {
            System.out.println("yayan se");
            Logger.getLogger(up_pres.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
